class Solution {
  public:
    pair<int, int> getMinMax(vector<int> arr) {
        pair<int,int> ans;
        int mn=100000000;
        int mx=-10000000;
       for(int i=0;i<arr.size();i++){
              mn=min(mn,arr[i]);
              mx=max(mx,arr[i]);
       }
  
        ans.first = mn;
        ans.second = mx; 
        return ans;
    }
};

